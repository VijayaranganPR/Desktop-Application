/*
To show some results in the console from the code we use Console.log() (mostly like a print statement).
*/

let name = "Vijayarangan";
let age = 21;

// one way
console.log("My name is "+ name + " and my age is "+ age);

// interpolation way of printing 
console.log(`My name is ${name} and my age is ${age}`)

// in table
console.table([1,2,3,4,5])      // prints a table

