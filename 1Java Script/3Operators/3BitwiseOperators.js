/*
Bitwise Operators
& - Bitwise AND
| - Bitwise OR
~ - Bitwise NOT
^ - Bitwise XOR
<< - Bitwise left shift
>> - Bitwise right shift
>>> - unsigned right shift
*/

let a = 5;
let b = 1;

// & - Bitwise AND
console.log(`Bitwise & of ${a} and ${b} is ${a&b}`)

//| - Bitwise OR
console.log(`Bitwise | of ${a} and ${b} is ${a|b}`)

//~ - Bitwise NOT
console.log(`Bitwise ~ of ${a} and ${b} is ${~a}`)

//^ - Bitwise XOR
console.log(`Bitwise ^ of ${a} and ${b} is ${a^b}`)

//<< - Bitwise left shift
console.log(`Bitwise << of ${a} and ${b} is ${a<<b}`)

//>> - Bitwise left shift
console.log(`Bitwise >> of ${a} and ${b} is ${a>>b}`)

//>>> - unsigned right shift
console.log(`Bitwise >>> of ${a} and ${b} is ${a>>>b}`)
