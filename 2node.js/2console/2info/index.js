/*
In practice, the distinction between console.log and console.info is not strictly enforced, 
and developers often use either method interchangeably. The main difference between the two methods is the icon used in the console to represent the message. 
console.log messages are represented by a simple "ℹ️" icon, 
while console.info messages are represented by an "i" in a circle.




*/
console.info('hi')

a = 10 
console.info(a)

console.info(`the value of a is ${a}`);
