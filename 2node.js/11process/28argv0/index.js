/*
In Node.js, the process.argv0 property is a string that contains the value of the first element of the process.argv array, 
which is the name or path of the Node.js executable that was used to start the process. 
It can be useful when you want to check or use the path of the Node.js binary, 
or when the name of the Node.js binary has been changed or could be different depending on the environment.
*/

console.log(process.argv0)

/*
node
*/
